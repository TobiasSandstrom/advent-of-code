export const day2 = (input: string) => {
  // Rock X 1 OR A
  // Paper Y 2  OR B
  // Scissor Z 3 OR C

  // Loss 0
  // Draw 3
  // Win 6

  let sortedinput = input.split(/\r?\n/);
  let res = 0;
  for (const game of sortedinput) {
    res = res + play(game);
  }
  console.log("Part 1 result ---->", res);

  // PART 2
  // X I need to lose
  // Y I need to draw
  // Z I need to Win

  let part2res = 0;

  for (const game of sortedinput) {
    switch (game.charAt(2)) {
      case "X":
        part2res = part2res + playToLose(game);
        continue;
      case "Y":
        part2res = part2res + playToDraw(game);
        continue;
      case "Z":
        part2res = part2res + playToWin(game);
        continue;
    }
  }
  console.log("Part 2 result ---->", part2res);
};

